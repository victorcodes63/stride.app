async function main() {
  console.log('Seeding 10 outsourcing clients...');

  const created = [];
  for (const seed of OUTSOURCING_CLIENT_SEEDS) {
    const client = await prisma.outsourcingClient.create({
      data: {
        name: seed.name,
        contactName: seed.contactName,
        contactEmail: seed.contactEmail,
        contactPhone: seed.contactPhone,
        kraPin: seed.kraPin,
        nssfEmployerNumber: seed.nssfEmployerNumber,
        nhifEmployerNumber: seed.nhifEmployerNumber,
        companyRegistrationNumber: seed.companyRegistrationNumber,
        vatNumber: seed.vatNumber,
        bankName: seed.bankName,
        bankAccountNumber: seed.bankAccountNumber,
        bankBranch: seed.bankBranch,
        bankSwiftCode: seed.bankSwiftCode,
        currency: seed.currency,
        billingCycle: seed.billingCycle,
        serviceFeeType: seed.serviceFeeType,
        serviceFeeAmount: seed.serviceFeeAmount,
        paymentTerms: seed.paymentTerms,
        postalAddress: seed.postalAddress,
        county: seed.county,
        contractStartDate: seed.contractStartDate,
        contractEndDate: seed.contractEndDate,
      },
    });
    created.push(client.name);
    console.log(`  Created: ${client.name}`);
  }

  console.log(`\nDone. ${created.length} outsourcing clients seeded.`);
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
